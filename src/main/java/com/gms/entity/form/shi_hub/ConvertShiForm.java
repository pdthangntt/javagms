package com.gms.entity.form.shi_hub;


/**
 *
 * @author pdThang
 */
public class ConvertShiForm {

    private String indicator_code;
    private String district_code;
    private String site_code;

    private String value;
    private String sex;
    private String age_group;
    private String key_population;
    private String type;

    public String getIndicator_code() {
        return indicator_code;
    }

    public void setIndicator_code(String indicator_code) {
        this.indicator_code = indicator_code;
    }

    public String getDistrict_code() {
        return district_code;
    }

    public void setDistrict_code(String district_code) {
        this.district_code = district_code;
    }

    public String getSite_code() {
        return site_code;
    }

    public void setSite_code(String site_code) {
        this.site_code = site_code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getAge_group() {
        return age_group;
    }

    public void setAge_group(String age_group) {
        this.age_group = age_group;
    }

    public String getKey_population() {
        return key_population;
    }

    public void setKey_population(String key_population) {
        this.key_population = key_population;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
