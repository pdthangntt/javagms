package com.gms.entity.form.htc_confirm;

/**
 *
 * @author pdThang
 */
public class ReceivedForm {

    private String code;
    private String messageVisit;
    private String messageConfirm;
    private String siteConfirm;
    private String title;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMessageVisit() {
        return messageVisit;
    }

    public void setMessageVisit(String messageVisit) {
        this.messageVisit = messageVisit;
    }

    public String getMessageConfirm() {
        return messageConfirm;
    }

    public void setMessageConfirm(String messageConfirm) {
        this.messageConfirm = messageConfirm;
    }

    public String getSiteConfirm() {
        return siteConfirm;
    }

    public void setSiteConfirm(String siteConfirm) {
        this.siteConfirm = siteConfirm;
    }

}
